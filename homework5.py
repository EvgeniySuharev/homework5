# Работа со списками:
my_list = ['apple', 'banana', 'kiwi', 'lemon', 'mango', 'pineapple']
print('My list:', my_list)
print('First element:', my_list[0])
print('Last element', my_list[-1])
print('Sublist:', my_list[2:5])
my_list[2] = 'orange'
print('My new list:', my_list)
# Работа со словарями:
my_dict = {'black': 'черный', 'white': 'белый', 'green': 'зеленый'}
print('My dictionary:', my_dict)
print('Translation: Black -', my_dict['black'])
my_dict['black'] = 'розовый'
my_dict['grey'] = 'серый'
print('My new dictionary:', my_dict)
